package com.kooapps.wordsearch.model.core.helper;

public class Range {

    private int mMin;
    private int mMax;

    public Range(int min, int max) {
        mMin = min;
        mMax = max;
    }

    public int getMin() {
        return mMin;
    }

    public int getMax() {
        return mMax;
    }

}
